package basicJava;

public class Loops {
	public static void main(String args[]) {
		System.out.println("Hello");
		int salary = 1001;
		if (salary>10000) {
			salary+=2000;
		} else {
			salary+=1000;
		}
		System.out.println(salary);
		
		for (int i = 0; i < 2; i++) {
			System.out.println(i);
		}
		
		
	}

}
